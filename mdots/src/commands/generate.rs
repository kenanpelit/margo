/// Generate the content for service.lua
fn generate_service_lua_content() -> String {
    r#"-- Service Detection Module for mdots
-- This module automatically detects running services and configures them
-- Generated by: mdots generate service

local service = {}

-- Get desktop environment services
function service.get_desktop_services()
    local services = {
        enabled = {},
        disabled = {}
    }

    -- Network Manager
    if mdots.service.is_active("NetworkManager") then
        table.insert(services.enabled, "NetworkManager.service")
    end

    -- Bluetooth
    if mdots.service.is_active("bluetooth") then
        table.insert(services.enabled, "bluetooth.service")
    end

    -- Display Manager detection
    if mdots.service.is_active("sddm") then
        table.insert(services.enabled, "sddm.service")
    elseif mdots.service.is_active("gdm") then
        table.insert(services.enabled, "gdm.service")
    elseif mdots.service.is_active("lightdm") then
        table.insert(services.enabled, "lightdm.service")
    end

    return services
end

-- Get audio services
function service.get_audio_services()
    local services = {
        enabled = {},
        disabled = {}
    }

    -- PulseAudio (user service)
    if mdots.service.is_user_service("pulseaudio") then
        -- PulseAudio is socket-activated, ensure it's available
        mdots.log.info("PulseAudio user service detected")
    end

    -- PipeWire (user services)
    if mdots.service.is_user_service("pipewire") then
        mdots.log.info("PipeWire detected")
    end

    return services
end

-- Get system services
function service.get_system_services()
    local services = {
        enabled = {},
        disabled = {}
    }

    -- SSH
    if mdots.service.is_enabled("sshd") then
        table.insert(services.enabled, "sshd.service")
    end

    -- Docker
    if mdots.service.is_enabled("docker") then
        table.insert(services.enabled, "docker.service")
    end

    -- Firewall
    if mdots.service.is_active("ufw") then
        table.insert(services.enabled, "ufw.service")
    elseif mdots.service.is_active("firewalld") then
        table.insert(services.enabled, "firewalld.service")
    end

    -- Power management for laptops
    if mdots.hardware.is_laptop() then
        if mdots.service.is_active("tlp") then
            table.insert(services.enabled, "tlp.service")
        end
    end

    return services
end

-- Combine all services
function service.get_all_services()
    local all_services = {
        enabled = {},
        disabled = {}
    }

    -- Merge desktop services
    local desktop = service.get_desktop_services()
    for _, svc in ipairs(desktop.enabled) do
        table.insert(all_services.enabled, svc)
    end

    -- Merge system services
    local system = service.get_system_services()
    for _, svc in ipairs(system.enabled) do
        table.insert(all_services.enabled, svc)
    end

    return all_services
end

-- Export module configuration
return {
    description = "Auto-detected service configuration for " .. mdots.system.hostname(),
    services = service.get_all_services(),

    -- Metadata for display
    metadata = {
        hostname = mdots.system.hostname(),
        detected_at = "auto-generated",
    }
}
"#
    .to_string()
}

