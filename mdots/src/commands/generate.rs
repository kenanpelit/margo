use crate::config::ConfigPaths;

/// Check if Lua support is available (either config.lua or host.lua files exist)
fn check_lua_support(paths: &ConfigPaths) -> Result<()> {
    let config_lua = paths.config_dir.join("config.lua");

    // Check for config.lua
    if config_lua.exists() {
        return Ok(());
    }

    // Check for any .lua files in hosts directory
    let hosts_dir = paths.config_dir.join("hosts");
    if hosts_dir.exists() {
        if let Ok(entries) = fs::read_dir(&hosts_dir) {
            for entry in entries.filter_map(|e| e.ok()) {
                let path = entry.path();
                if path.extension().and_then(|s| s.to_str()) == Some("lua") {
                    return Ok(());
                }
            }
        }
    }

    // No Lua files found - provide helpful error
    Err(anyhow!(
        "{}\n\n\
            Lua modules require Lua configuration support to be set up first.\n\n\
            You have two options:\n\
            1. Convert your config to Lua:\n\
               - Move {} to {}\n\
               - Update the syntax to Lua format (see LUA-HOSTS.md for examples)\n\n\
            2. Use the advanced init to set up a new Lua-based config:\n\
               - {}\n\n\
            For more information, see:\n\
               - LUA-MODULES.md for Lua module format\n\
               - LUA-HOSTS.md for Lua host configuration\n\
               - MDOTS-LUA-API.md for available APIs",
        "Lua support not detected!".red().bold(),
        "config.yaml".cyan(),
        "config.lua".cyan(),
        "mdots init --adv".cyan()
    ))
}

/// Generate hardware.lua module with auto-detection
pub fn hardware(paths: &ConfigPaths, force: bool) -> Result<()> {
    // Check for Lua support first
    check_lua_support(paths)?;

    println!("{}", "=== Generate hardware.lua ===".blue());
    println!();

    let modules_dir = paths.modules_dir();
    let hardware_file = modules_dir.join("hardware.lua");

    // Check if file already exists
    if hardware_file.exists() && !force {
        println!(
            "{} hardware.lua already exists at: {}",
            "!".yellow(),
            hardware_file.display()
        );
        print!("Overwrite? [y/N] ");
        io::stdout().flush()?;

        let mut input = String::new();
        io::stdin().read_line(&mut input)?;

        if !input.trim().eq_ignore_ascii_case("y") {
            println!("{}", "Cancelled".yellow());
            return Ok(());
        }
    }

    // Ensure modules directory exists
    fs::create_dir_all(&modules_dir).context("Failed to create modules directory")?;

    // Generate the hardware.lua content
    let content = generate_hardware_lua_content();

    // Write the file
    fs::write(&hardware_file, &content).context("Failed to write hardware.lua")?;

    println!("{} Created: {}", "✓".green(), hardware_file.display());
    println!();
    println!("{}", "Hardware Detection Module Generated!".green());
    println!();
    println!("This module will automatically detect:");
    println!(
        "  {} CPU vendor (Intel/AMD) and install appropriate microcode",
        "•".cyan()
    );
    println!(
        "  {} GPU vendor(s) and install appropriate drivers",
        "•".cyan()
    );
    println!(
        "  {} Laptop vs Desktop and install power management tools",
        "•".cyan()
    );
    println!();
    println!("Next steps:");
    println!(
        "  1. Review the module: {}",
        "cat ~/.config/arch-config/modules/hardware.lua".cyan()
    );
    println!(
        "  2. Enable the module: {}",
        "mdots module enable hardware".cyan()
    );
    println!("  3. Preview changes:   {}", "mdots sync --dry-run".cyan());
    println!();

    Ok(())
}

/// Generate the content for hardware.lua
fn generate_hardware_lua_content() -> String {
    r#"-- Hardware Detection Module for mdots
-- This module automatically detects your hardware and selects appropriate packages
-- Generated by: mdots generate hardware

local hardware = {}

-- Get CPU vendor
function hardware.get_cpu_packages()
    local packages = {}
    local cpu = mdots.hardware.cpu_vendor()

    if cpu == "intel" then
        table.insert(packages, "intel-ucode")
    elseif cpu == "amd" then
        table.insert(packages, "amd-ucode")
    end

    return packages
end

-- Get GPU packages based on detected hardware
function hardware.get_gpu_packages()
    local packages = {}
    local is_laptop = mdots.hardware.is_laptop()

    -- Always include base mesa for OpenGL
    table.insert(packages, "mesa")

    if mdots.hardware.has_nvidia() then
        if is_laptop then
            -- Laptop: Use DKMS driver for hybrid graphics
            table.insert(packages, "nvidia-dkms")
            table.insert(packages, "nvidia-utils")
            table.insert(packages, "nvidia-settings")
            table.insert(packages, "nvidia-prime")  -- For GPU switching
            -- Also need Intel/AMD integrated graphics packages
        else
            -- Desktop: Use standard driver
            table.insert(packages, "nvidia")
            table.insert(packages, "nvidia-utils")
            table.insert(packages, "nvidia-settings")
        end
        table.insert(packages, "lib32-nvidia-utils")  -- 32-bit support for games
    end

    if mdots.hardware.has_amd_gpu() then
        table.insert(packages, "vulkan-radeon")
        table.insert(packages, "lib32-vulkan-radeon")
        table.insert(packages, "libva-mesa-driver")  -- Hardware video acceleration
        table.insert(packages, "lib32-mesa")
    end

    if mdots.hardware.has_intel_gpu() then
        table.insert(packages, "vulkan-intel")
        table.insert(packages, "lib32-vulkan-intel")
        table.insert(packages, "intel-media-driver")  -- Hardware video acceleration
        table.insert(packages, "lib32-mesa")
    end

    return packages
end

-- Get laptop-specific packages
function hardware.get_laptop_packages()
    local packages = {}

    if mdots.hardware.is_laptop() then
        table.insert(packages, "tlp")           -- Power management
        table.insert(packages, "tlp-rdw")       -- Radio device wizard
        table.insert(packages, "powertop")      -- Power consumption analyzer
        -- Uncomment if you want brightness control
        -- table.insert(packages, "brightnessctl")
    end

    return packages
end

-- Get services to enable/disable based on hardware
function hardware.get_services()
    local services = {
        enabled = {},
        disabled = {}
    }

    if mdots.hardware.is_laptop() then
        table.insert(services.enabled, "tlp")
    end

    return services
end

-- Combine all packages
function hardware.get_all_packages()
    local packages = {}

    -- Add CPU packages
    for _, pkg in ipairs(hardware.get_cpu_packages()) do
        table.insert(packages, pkg)
    end

    -- Add GPU packages
    for _, pkg in ipairs(hardware.get_gpu_packages()) do
        table.insert(packages, pkg)
    end

    -- Add laptop packages
    for _, pkg in ipairs(hardware.get_laptop_packages()) do
        table.insert(packages, pkg)
    end

    -- Always include firmware
    table.insert(packages, "linux-firmware")

    return packages
end

-- Build metadata for display
function hardware.get_metadata()
    return {
        cpu = mdots.hardware.cpu_vendor(),
        gpus = mdots.hardware.gpu_vendors(),
        is_laptop = mdots.hardware.is_laptop(),
        has_battery = mdots.hardware.has_battery(),
        chassis = mdots.hardware.chassis_type(),
        hostname = mdots.system.hostname(),
    }
end

-- Export module configuration
return {
    description = "Auto-detected hardware configuration for " .. mdots.system.hostname(),
    packages = hardware.get_all_packages(),
    services = hardware.get_services(),
    metadata = hardware.get_metadata(),

    -- Optional: Add hooks for hardware-specific setup
    -- pre_install_hook = "scripts/hardware-pre.sh",
    -- post_install_hook = "scripts/hardware-post.sh",
}
"#
    .to_string()
}

