use crate::json_struct::MatugenTheme;

/// Emit the colour roles `mlogind` (margo's TUI login manager) reads as
/// `$`-variables in its `variables.toml`. Maps the Material palette onto
/// the greeter's theming slots so the login screen matches the wallpaper.
/// Must stay in sync with `mlogind/extra/config.toml`'s `$`-names.
pub fn to_mlogind_variables(theme: &MatugenTheme) -> String {
    let c = &theme.colors;
    let mode = &theme.mode;
    macro_rules! col {
        ($field:expr) => {
            match mode.as_str() {
                "dark" => &$field.dark.color,
                "light" => &$field.light.color,
                _ => &$field.default.color,
            }
        };
    }
    format!(
        "# Generated by margo's matugen — mlogind colour variables.\n\
         # Overwritten on every wallpaper/theme change; `mlogind sync-theme`\n\
         # copies this into /etc/mlogind/variables.toml for the greeter.\n\
         bg           = \"{bg}\"\n\
         panel_border = \"{panel_border}\"\n\
         field_border = \"{field_border}\"\n\
         text         = \"{text}\"\n\
         subtext      = \"{subtext}\"\n\
         muted        = \"{muted}\"\n\
         accent       = \"{accent}\"\n\
         accent_soft  = \"{accent_soft}\"\n\
         danger       = \"{danger}\"\n\
         login_title    = \"User\"\n\
         password_title = \"Password\"\n",
        bg = col!(c.surface),
        panel_border = col!(c.surface_variant),
        field_border = col!(c.outline),
        text = col!(c.on_surface),
        subtext = col!(c.on_surface_variant),
        muted = col!(c.on_surface_variant),
        accent = col!(c.primary),
        accent_soft = col!(c.tertiary),
        danger = col!(c.error),
    )
}

