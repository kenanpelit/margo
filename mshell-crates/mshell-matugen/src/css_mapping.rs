/// Build the `~/.cache/margo/mshell-colors.toml` sidecar that external,
/// out-of-process panels read so they stay in step with the shell's
/// wallpaper scheme without linking to mshell. Today's consumer is the
/// standalone `mvpn menu` panel; the schema mirrors what its theme loader
/// expects (`[appearance]` with `background_color`/`primary_color` tables,
/// `#rrggbb` hex). Without this the file went stale and panels rendered a
/// month-old palette (the "why is mvpn brown" bug).
pub fn to_mshell_colors(theme: &MatugenTheme) -> String {
    let c = &theme.colors;
    let mode = &theme.mode;

    macro_rules! col {
        ($field:expr) => {
            match mode.as_str() {
                "dark" => $field.dark.color.trim(),
                "light" => $field.light.color.trim(),
                _ => $field.default.color.trim(),
            }
        };
    }

    format!(
        "# Auto-generated by mshell from the matugen palette — do not edit.\n\
         # Read by external panels (e.g. `mvpn menu`) to match the shell.\n\
         \n\
         [appearance]\n\
         background_color = {{ base = \"{base}\", weak = \"{weak}\", neutral = \"{neutral}\", text = \"{text}\" }}\n\
         primary_color    = {{ base = \"{primary}\", text = \"{on_primary}\" }}\n\
         danger_color     = \"{danger}\"\n\
         text_color       = \"{text}\"\n",
        base = col!(c.surface),
        weak = col!(c.surface_container),
        neutral = col!(c.surface_container_high),
        text = col!(c.on_surface),
        primary = col!(c.primary),
        on_primary = col!(c.on_primary),
        danger = col!(c.error),
    )
}

/// Build margo `.conf` window-border colour overrides from the same
/// palette. Maps Material roles → margo's border colour keys so the
/// compositor's window borders track the wallpaper scheme alongside the
/// shell UI. `rootcolor` is intentionally left to the user's static
/// config. Emitted as `0xRRGGBBAA` (margo's `parse_color` accepts that).
pub fn to_margo_colors(theme: &MatugenTheme) -> String {
    let c = &theme.colors;
    let mode = &theme.mode;

    macro_rules! col {
        ($field:expr) => {
            match mode.as_str() {
                "dark" => &$field.dark.color,
                "light" => &$field.light.color,
                _ => &$field.default.color,
            }
        };
    }

    // matugen `--json hex` yields `#rrggbb`; margo wants `0xrrggbbAA`.
    fn margo(hex: &str, alpha: &str) -> String {
        let rgb: String = hex
            .trim()
            .trim_start_matches('#')
            .trim_start_matches("0x")
            .chars()
            .take(6)
            .collect();
        format!("0x{rgb}{alpha}")
    }

    format!(
        "# Auto-generated by mshell from the matugen palette — do not edit.\n\
         # `source`d from config.conf; overrides the static border colours.\n\
         focuscolor      = {focus}\n\
         bordercolor     = {border}\n\
         urgentcolor     = {urgent}\n\
         scratchpadcolor = {scratch}\n\
         globalcolor     = {global}\n\
         overlaycolor    = {overlay}\n\
         shadowscolor    = {shadow}\n",
        focus = margo(col!(c.primary), "ff"),
        border = margo(col!(c.outline), "ff"),
        urgent = margo(col!(c.error), "ff"),
        scratch = margo(col!(c.tertiary), "ff"),
        global = margo(col!(c.secondary), "ff"),
        overlay = margo(col!(c.primary_container), "ff"),
        // Window glow: a SUBTLE ambient lift, not a frame. Muted
        // `primary_container` (desaturated, distinct from the bright
        // `primary` border) at ~8% alpha — low enough that the glow stays
        // below the content in the visual hierarchy (Material/GNOME feel
        // the halo at ~5–10% of the surface). Paired with a moderate,
        // tight blur in config.conf so it hugs the window instead of
        // sprawling. (Was 0x26/~15%, which competed with the content; the
        // even older default was the dark `shadow` role at 0x99.)
        shadow = margo(col!(c.primary_container), "14"),
    )
}
